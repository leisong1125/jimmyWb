import Foundation
import UIKit

func CGRectMake(_ x:CGFloat, _ y:CGFloat, _ width:CGFloat, _ heigth:CGFloat) -> CGRect {
    return CGRect(x:x, y:y, width:width, height:heigth)
}

func CGPointMake(_ x:CGFloat, _ y:CGFloat) -> CGPoint {
    return CGPoint(x:x, y:y)
}
